package obj;

// 학적변동 클래스
public class StudentStatusModify {
	private String student_num;
	private String year_and_semester;
	private String modified_date;
	private String modified_status;

	public String getStudent_num() {
		return student_num;
	}
	public void setStudent_num(String student_num) {
		this.student_num = student_num;
	}
	
	public String getYear_and_semester() {
		return year_and_semester;
	}

	public void setYear_and_semester(String year_and_semester) {
		this.year_and_semester = year_and_semester;
	}

	public String getModified_date() {
		return modified_date;
	}
	public void setModified_date(String modified_date) {
		this.modified_date = modified_date;
	}
	public String getModified_status() {
		return modified_status;
	}
	public void setModified_status(String modified_status) {
		this.modified_status = modified_status;
	}
	
	
	
}
